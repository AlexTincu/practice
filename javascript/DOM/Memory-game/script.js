const images = [];
let tempElem1 = "";
let tempElem2 = "";
let click = -1;
let win = 0;
let score = 0;
let time = 0;

const preElem = document.querySelector("#pre");
const themesElem = document.querySelector("#themes");
const boxElems = document.getElementsByClassName("box");
const mainElem = document.querySelector(".main");
const timeElem = document.querySelector("#time");
const scoreElem = document.querySelector("#score");
const postElem = document.querySelector("#post");
const finalElem = document.querySelector("#final");
const againElem = document.querySelector("#again");

// initiate the game with chosen theme
themesElem.addEventListener("click", function (e) {
  if (e.target.classList.contains("themes")) {
    activateTheme(e.target.id);
    preElem.classList.add("hidden");
  }
});

function activateTheme(theme) {
  // insert theme in images array
  for (let i = 0; i < 20; i++) {
    images.push(library[theme][i]);
  }
  // insert images in memory game
  for (let i = 0; i < 20; i++) {
    var rand = Math.floor(Math.random() * (images.length - 1));
    boxElems[i].innerHTML =
      "<img src='" + images[rand] + "' alt='image' class='hidden'>";
    images.splice(rand, 1);
  }
}

// Handle the play
mainElem.addEventListener("click", gameLogic);

function gameLogic(e) {
  // make sure the box is playable
  if (e.target.classList.contains("play")) {
    e.target.firstChild.classList.remove("hidden");
    // first of two click
    if (click < 1) {
      tempElem1 = e.target;
      // timer
      if (click === -1) {
        timer = setInterval(function () {
          time++;
          timeElem.innerHTML = time;
        }, 1000);
      }
      click = 1;
    }

    // second click
    else if (e.target !== tempElem1) {
      tempElem2 = e.target;

      // different images
      if (tempElem1.firstChild.src !== tempElem2.firstChild.src) {
        mainElem.removeEventListener("click", gameLogic);
        setTimeout(function () {
          tempElem1.firstChild.classList.add("hidden");
          tempElem2.firstChild.classList.add("hidden");
          mainElem.addEventListener("click", gameLogic);
        }, 400);
        if (score > 0) {
          score -= 2;
        }
        scoreElem.innerHTML = score;
      }

      // same images
      else {
        score += 10;
        win += 2;
        tempElem1.firstChild.classList.add("outlined");
        tempElem2.firstChild.classList.add("outlined");
        tempElem1.classList.remove("play");
        tempElem2.classList.remove("play");
        scoreElem.innerHTML = score;

        // game won
        if (win === 20) {
          clearInterval(timer);
          finalElem.innerHTML =
            "You won " + score + " points <br> in " + time + " seconds";
          postElem.classList.remove("hidden");
        }
      }
      click = 0;
    }
  }
}

againElem.addEventListener("click", resetGame);

function resetGame() {
  // reset game
  tempElem1 = "";
  tempElem2 = "";
  click = -1;
  win = 0;
  score = 0;
  time = 0;
  postElem.classList.add("hidden");
  preElem.classList.remove("hidden");
  for (let i = 0; i < 20; i++) {
    boxElems[i].classList.add("play");
    boxElems[i].firstChild.classList.add("hidden");
  }
  timeElem.textContent = time;
  scoreElem.textContent = score;
}

